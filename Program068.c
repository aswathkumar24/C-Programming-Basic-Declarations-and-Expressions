/******************************************************************************

Write a C program that prints the powers of 2 table for the powers 0 to 10, both positive and negative.
=======================================
n 2 to power n 2 to power -n
=======================================
0 1 1.000000000000
1 2 0.500000000000
2 4 0.250000000000
3 8 0.125000000000
4 16 0.062500000000
5 32 0.031250000000
6 64 0.015625000000
7 128 0.007812500000
8 256 0.003906250000
9 512 0.001953125000
10 1024 0.000976562500
======================================

*******************************************************************************/
#include <stdio.h>      //Program 68

void square(void)
{
    int y=1,x=2;
    double a;
    for(int i=0;i<=10;i++)
    {
        if(i==0)
        {
            y = 1;
            a = 1;
        }
        else if(i>0)
        {
            y=1,a=1;
            for(int j=0;j<i;j++)
            {
                y=y*x;
                a=a*x;
            }
        }
        printf("%d\t%d\t%lf\n",i,y,1/a);
    }
}

int main()
{
   square();
    return 0;
}
